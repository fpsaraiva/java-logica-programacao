package aula1104;

public class TiposNumericos {
    public static void main(String[] args) {
        //numeros inteiros
        byte numeroInteiro1 = 1;
        System.out.println("O primeiro número é: " + numeroInteiro1);

        short numeroInteiro2 = 2;
        System.out.println("O segundo número é: " + numeroInteiro2);

        int anoAtual = 2024;
        System.out.println("O ano atual é: " + anoAtual);

        long idUsuario = 2L;
        System.out.println("ID: " + idUsuario);

        //numeros ponto flutuante
        float notaMedia = 6.75f;
        System.out.println("A média final foi de: " + notaMedia);

        double salario = 100000.99;
        System.out.println("Seu salário inicial é de " + salario);
    }
}
